package program;

public class OnPressedexpr {
   public NavigatorPushNode navigatorPushNode;

    public OnPressedexpr(NavigatorPushNode navigatorPushNode) {
        this.navigatorPushNode = navigatorPushNode;
    }

    @Override
    public String toString() {
        return "\nOnPressedexpr{" +
                "\nnavigatorPushNode=" + navigatorPushNode +
                "\n}";
    }
}
