package program;

public class DeclerationVarDynamic extends decleration {
    public String ID ;
    public String type;
    public varType value ;


    public DeclerationVarDynamic(String ID, String type, varType value) {
        this.ID = ID;
        this.type = type;
        this.value = value;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
